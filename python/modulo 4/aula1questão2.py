# Solicita ao usuário que insira um valor para n
n = int(input("Digite um valor para n: "))

# Inicializa cont com 0
cont = 0

# Verifica se n é menor que cont
if n < cont:
    # Se a condição for verdadeira, incrementa cont e imprime o valor de cont
    cont = cont + 1
    print(cont)
else:
    # Se a condição for falsa, imprime "fim"
    print("fim")
