# Armazenando valores em variáveis
variavel1 = 5
variavel2 = 2

# Imprimindo o tipo de dados das duas variáveis
print("Tipo de variavel1:", type(variavel1))
print("Tipo de variavel2:", type(variavel2))

# Dividindo a primeira variável pela segunda e armazenando o resultado em uma terceira variável
resultado = variavel1 / variavel2

# Imprimindo o valor da terceira variável e o seu tipo
print("Resultado da divisão:", resultado)
print("Tipo de resultado:", type(resultado))