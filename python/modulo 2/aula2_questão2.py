# Armazenando os valores em variáveis
texto = "o resultado é"
valor_inteiro = 10
valor_float = 3.5

# Somando os valores da segunda e terceira variável
soma = valor_inteiro + valor_float

# Imprimindo todas as variáveis na ordem de criação e seus tipos
print("Texto:", texto)
print("Tipo do texto:", type(texto))

print("Valor inteiro:", valor_inteiro)
print("Tipo do valor inteiro:", type(valor_inteiro))

print("Valor float:", valor_float)
print("Tipo do valor float:", type(valor_float))

print("Soma:", soma)
print("Tipo da soma:", type(soma))